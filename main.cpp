    cpu.Reset(m);
    //Simple program
    m[0x8000] = 0xAD; m[0x8001] = 0x42; m[0x8002] = 0x42; m[0x4242] = 0x55; m[0x8003] = 0xA5; m[0x8004] = 0x50;
