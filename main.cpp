#include "CPU.h"

int main()
{
    CPU cpu;
    Memory m;
    cpu.Reset(m);
    //Simple program
    m[0x8000] = 0x20; m[0x8001] = 0x42; m[0x8002] = 0x42; m[0xFFF0] = 0x20; m[0xFFF1] = 0x50;
    m[0xFFF2] = 0x50; m[0x4242] = 0xA9; m[0x4243] = 0x93; m[0x4247] = 0xA5; m[0x4248] = 0x80;
    m[0x0080] = 0x12; m[0x4249] = 0x20; m[0x424A] = 0xF0; m[0x424B] = 0xFF; m[0x5050] = 0xAA;
    for(int i = 0; i < 9; i++) {
        //cpu.GetStatus();
        cpu.Execute(m);
        cpu.GetStatus();
    } 
}